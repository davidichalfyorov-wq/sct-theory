def jordan_frame_action(phi: float | mp.mpf,
                        R: float | mp.mpf,
                        C2: float | mp.mpf,
                        xi: float | mp.mpf = 0.0,
                        f0: float | mp.mpf = 1.0,
                        f2: float | mp.mpf = 1.0,
                        f4: float | mp.mpf = 1.0,
                        Lambda0: float | mp.mpf = 1.0) -> dict[str, mp.mpf]:
    """Evaluate the Jordan-frame spectral action density with Lambda = Lambda_0 e^phi.

    S_J = int sqrt{g} d^4x { V_0 e^{4phi} - (M_Pl^2/2) e^{2phi} R
          + (f_4/(16pi^2)) [alpha_C C^2 + alpha_R(xi) R^2] }

    where:
      V_0 = f_0 Lambda_0^4 / (4 pi^2)
      M_Pl^2 = f_2 Lambda_0^2 / (2 pi^2)

    Parameters:
        phi: dilaton field value (dimensionless)
        R: Ricci scalar (in Lambda_0^2 units)
        C2: C_{abcd}C^{abcd} (in Lambda_0^4 units)
        xi: Higgs non-minimal coupling
        f0, f2, f4: spectral action moments
        Lambda0: reference spectral scale

    Returns dict with each term and total.
    """
    mp.mp.dps = DPS
    phi_mp = mp.mpf(phi)
    R_mp = mp.mpf(R)
    C2_mp = mp.mpf(C2)
    xi_mp = mp.mpf(xi)
    f0_mp = mp.mpf(f0)
    f2_mp = mp.mpf(f2)
    f4_mp = mp.mpf(f4)
    L0 = mp.mpf(Lambda0)

    # Cosmological constant term
    V_0 = f0_mp * L0**4 / (4 * mp.pi**2)
    S_cc = V_0 * mp.exp(4 * phi_mp)

    # Einstein-Hilbert term (with sign for action, not Lagrangian)
    M_Pl_sq = f2_mp * L0**2 / (2 * mp.pi**2)
    S_EH = -(M_Pl_sq / 2) * mp.exp(2 * phi_mp) * R_mp

    # Curvature-squared terms
    alpha_R = ALPHA_R_FUNC(xi_mp)
    S_C2 = (f4_mp / (16 * mp.pi**2)) * ALPHA_C * C2_mp
    S_R2 = (f4_mp / (16 * mp.pi**2)) * alpha_R * R_mp**2

    return {
        "S_cc": S_cc,
        "S_EH": S_EH,
        "S_C2": S_C2,
        "S_R2": S_R2,
        "S_total": S_cc + S_EH + S_C2 + S_R2,
        "V_0": V_0,
        "M_Pl_sq": M_Pl_sq,
    }


def verify_jordan_frame() -> dict[str, Any]:
    """Verify the Jordan-frame action with specific test values."""
    mp.mp.dps = DPS

    results = {}

    # Test 1: phi = 0 reduces to standard spectral action
    jf0 = jordan_frame_action(phi=0, R=1.0, C2=0.5, xi=0.0)
    jfp = jordan_frame_action(phi=0.5, R=1.0, C2=0.5, xi=0.0)

    # e^{4*0.5} = e^2
    ratio_cc = jfp["S_cc"] / jf0["S_cc"]
    expected_ratio = mp.exp(2)
    results["cc_ratio_check"] = float(abs(ratio_cc - expected_ratio))
    assert abs(ratio_cc - expected_ratio) < mp.mpf("1e-90"), \
        f"CC ratio failed: {ratio_cc} vs {expected_ratio}"

    # EH ratio: e^{2*0.5}/e^0 = e
    ratio_EH = jfp["S_EH"] / jf0["S_EH"]
    expected_EH = mp.exp(1)
    results["EH_ratio_check"] = float(abs(ratio_EH - expected_EH))
    assert abs(ratio_EH - expected_EH) < mp.mpf("1e-90"), \
        f"EH ratio failed: {ratio_EH} vs {expected_EH}"

    # a_2 terms are phi-independent
    results["C2_phi_independent"] = float(abs(jfp["S_C2"] - jf0["S_C2"]))
    assert abs(jfp["S_C2"] - jf0["S_C2"]) < mp.mpf("1e-90"), \
        "C^2 term should be phi-independent"

    results["R2_phi_independent"] = float(abs(jfp["S_R2"] - jf0["S_R2"]))
    assert abs(jfp["S_R2"] - jf0["S_R2"]) < mp.mpf("1e-90"), \
        "R^2 term should be phi-independent"

    # Test 2: M_Pl^2 identification
    M_Pl_sq = jf0["M_Pl_sq"]
    expected_M_Pl_sq = mp.mpf(1) / (2 * mp.pi**2)  # f2=1, Lambda0=1
    results["M_Pl_sq_check"] = float(abs(M_Pl_sq - expected_M_Pl_sq))
    assert abs(M_Pl_sq - expected_M_Pl_sq) < mp.mpf("1e-90"), \
        f"M_Pl^2 failed: {M_Pl_sq} vs {expected_M_Pl_sq}"

    results["status"] = "PASS"
    return results


