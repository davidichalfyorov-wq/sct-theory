def dilaton_potential_tree(phi: float | mp.mpf,
                          f0: float | mp.mpf = 1.0,
                          Lambda0: float | mp.mpf = 1.0) -> mp.mpf:
    """Tree-level dilaton potential in the Einstein frame.

    V(phi) = V_0 = f_0 Lambda_0^4 / (4 pi^2)

    This is CONSTANT: the dilaton has a flat potential at tree level.
    The phi-dependence from e^{4phi} in the Jordan frame is exactly
    cancelled by the e^{-4phi} from the volume element rescaling.

    Returns V / Lambda_0^4.
    """
    mp.mp.dps = DPS
    # The potential is phi-INDEPENDENT
    return mp.mpf(f0) * mp.mpf(Lambda0)**4 / (4 * mp.pi**2)


def verify_flat_potential() -> dict[str, Any]:
    """Verify that the tree-level dilaton potential is exactly flat."""
    mp.mp.dps = DPS
    results = {}

    V_ref = dilaton_potential_tree(phi=0)

    # Test at many phi values: V must be identical
    phi_values = [mp.mpf(x) for x in
                  [-5, -2, -1, -0.5, 0, 0.01, 0.1, 0.5, 1, 2, 5, 10, 100]]
    max_deviation = mp.mpf(0)
    for phi_val in phi_values:
        V = dilaton_potential_tree(phi=phi_val)
        dev = abs(V - V_ref)
        if dev > max_deviation:
            max_deviation = dev

    results["max_deviation_from_flat"] = float(max_deviation)
    assert max_deviation < mp.mpf("1e-90"), \
        f"Potential is not flat: max deviation = {max_deviation}"
    results["flat_potential_verified"] = True
    results["V_0"] = float(V_ref)
    results["status"] = "PASS"
    return results


