    # epsilon ~ (4/chi_c^2) (chi/chi_c)^2 / (1 - (chi/chi_c)^2)^2
    # Near chi = 0: epsilon ~ 4 chi^2 / chi_c^4

    # The e-fold integral: N ~ chi_c^2 / (8 chi_end) for small-field
    # This gives r ~ 128 / (chi_c^4 N^3) which is very small

