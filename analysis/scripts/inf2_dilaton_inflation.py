def slow_roll_quadratic(N: int = 55) -> dict[str, Any]:
    """Slow-roll observables for a quadratic potential V = (1/2) m^2 chi^2.

    This is the simplest approximation to the CW potential near its minimum.

    n_s = 1 - 2/N
    r = 8/N

    Returns dict with observables and EXCLUDED status.
    """
