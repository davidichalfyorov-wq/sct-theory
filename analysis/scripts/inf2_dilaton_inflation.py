def canonical_dilaton(phi: float | mp.mpf) -> mp.mpf:
    """Convert dilaton phi to canonical field chi.

    chi = sqrt(6) * M_Pl * phi

    The canonical kinetic term is -(1/2)(d chi)^2 in Lorentzian signature.

    Parameters:
        phi: dimensionless dilaton field

    Returns chi / M_Pl.
    """
    return mp.sqrt(6) * mp.mpf(phi)


def inverse_canonical_dilaton(chi_over_MPl: float | mp.mpf) -> mp.mpf:
    """Convert canonical chi/M_Pl back to dilaton phi.

    phi = chi / (sqrt(6) * M_Pl)
    """
    return mp.mpf(chi_over_MPl) / mp.sqrt(6)


def verify_canonical_normalization() -> dict[str, Any]:
    """Verify the canonical normalization of the dilaton."""
    mp.mp.dps = DPS
    results = {}

    # chi = sqrt(6) M_Pl phi
    # Kinetic term: 3 M_Pl^2 (d phi)^2 = 3 M_Pl^2 (d chi)^2 / (6 M_Pl^2)
    #             = (1/2) (d chi)^2  ✓
    kinetic_ratio = 3 / (mp.sqrt(6)**2)
    expected = mp.mpf(1) / 2
    results["kinetic_normalization"] = float(abs(kinetic_ratio - expected))
    assert abs(kinetic_ratio - expected) < mp.mpf("1e-90"), \
        f"Kinetic normalization: 3/6 = {kinetic_ratio}, expected {expected}"

    # Round-trip test
    for phi_val in [0.0, 0.1, 1.0, -2.0, 5.0]:
        chi = canonical_dilaton(phi_val)
        phi_back = inverse_canonical_dilaton(chi)
        assert abs(phi_back - mp.mpf(phi_val)) < mp.mpf("1e-90"), \
            f"Round-trip failed at phi={phi_val}"
    results["round_trip"] = "PASS"

    results["status"] = "PASS"
    return results


