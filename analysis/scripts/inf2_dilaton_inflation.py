def r_squared_einstein_frame(
    R_E: float | mp.mpf,
    grad_phi_sq: float | mp.mpf,
    xi: float | mp.mpf = 0.0,
) -> dict[str, mp.mpf]:
    """Decompose the R^2 term in the Einstein frame.

    Under g_E = e^{2phi} g:
      R^2(g) sqrt{g} -> [R_E - 6(d phi)^2]^2 sqrt{g_E}
      (after IBP on Box phi terms)

    = R_E^2 - 12 R_E (d phi)^2 + 36 ((d phi)^2)^2

    Note: This contains NO potential term V(phi).
    The R^2 sector modifies the kinetic structure only.

    Parameters:
        R_E: Einstein-frame Ricci scalar
        grad_phi_sq: (d_E phi)^2
        xi: Higgs non-minimal coupling

    Returns dict with each contribution (in units of f_4/(16pi^2)).
    """
    mp.mp.dps = DPS
    R = mp.mpf(R_E)
    g2 = mp.mpf(grad_phi_sq)
    alpha_R = ALPHA_R_FUNC(mp.mpf(xi))

    # Pure R^2 (same as without dilaton)
    term_R2 = alpha_R * R**2

    # Cross-term: R * (d phi)^2 non-minimal coupling
    term_cross = -12 * alpha_R * R * g2

    # Quartic kinetic: ((d phi)^2)^2
    term_quartic = 36 * alpha_R * g2**2

    return {
        "alpha_R": float(alpha_R),
        "R2_pure": term_R2,
        "R_grad2_cross": term_cross,
        "grad4_quartic": term_quartic,
        "total": term_R2 + term_cross + term_quartic,
        "generates_potential": False,
    }


def verify_r2_no_potential() -> dict[str, Any]:
    """Verify that the R^2 sector does not generate a dilaton potential.

    On flat background (R_E = 0) with static dilaton (grad phi = 0):
    All R^2 contributions vanish identically.
    """
    mp.mp.dps = DPS
    results = {}

    # On Minkowski with static phi:
    r2_flat = r_squared_einstein_frame(R_E=0, grad_phi_sq=0, xi=0)
    assert r2_flat["R2_pure"] == 0, "R^2 should vanish on flat background"
    assert r2_flat["R_grad2_cross"] == 0, "Cross-term should vanish"
    assert r2_flat["grad4_quartic"] == 0, "Quartic should vanish"
    assert r2_flat["generates_potential"] is False, "Should not generate potential"

    results["flat_background"] = "All R^2 terms = 0"
    results["generates_potential"] = False

    # With curvature but static phi:
    r2_curved = r_squared_einstein_frame(R_E=1.0, grad_phi_sq=0, xi=0)
    # Only R_E^2 survives — this is just the original R^2 action
    expected = ALPHA_R_FUNC(0) * mp.mpf(1)**2
    assert abs(r2_curved["R2_pure"] - expected) < mp.mpf("1e-90"), \
        "Pure R^2 should survive"
    assert r2_curved["R_grad2_cross"] == 0, "Cross-term should vanish"
    assert r2_curved["grad4_quartic"] == 0, "Quartic should vanish"

    results["curved_static_phi"] = "Only R_E^2 term survives (no phi potential)"
    results["status"] = "PASS"
    return results


