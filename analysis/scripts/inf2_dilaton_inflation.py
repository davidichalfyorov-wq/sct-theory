def slow_roll_cw_hilltop(N: int = 55,
                         alpha_cw: float | mp.mpf = 1e-13) -> dict[str, Any]:
    """Slow-roll observables for the CW hilltop potential.

    V = V_0 [1 - alpha (chi/v)^4 + ...]  near the hilltop at chi = 0.

    This is small-field inflation with:
      n_s = 1 - 2/N - 6 alpha / (N^2 v^2)  (approximately)
      r = 128 alpha / (N^3 v^4)  (very small)

    For the CW potential, the fine-tuning requirement alpha ~ 10^{-13}
    is comparable to the Higgs inflation fine-tuning.

    Returns dict with observables.
    """
    mp.mp.dps = DPS

