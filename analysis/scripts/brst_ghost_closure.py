def verify_clifford_algebra(gamma: np.ndarray, gamma5: np.ndarray) -> dict[str, Any]:
    """Verify all Clifford algebra relations and chirality properties.

    Checks:
        1. {gamma^a, gamma^b} = 2 delta^{ab}
        2. {gamma^a, gamma_5} = 0 for all a
        3. gamma_5^2 = I
        4. gamma_5 = gamma^0 gamma^1 gamma^2 gamma^3
        5. [sigma^{ab}, gamma_5] = 0 for all a, b
    """
    I4 = np.eye(4, dtype=complex)
    results = {"checks": [], "all_pass": True}

    # Check 1: anticommutation relations
    for a in range(4):
        for b in range(4):
            anticomm = gamma[a] @ gamma[b] + gamma[b] @ gamma[a]
            expected = 2 * (1 if a == b else 0) * I4
            err = norm(anticomm - expected)
            ok = err < HERMITIAN_TOL
            if not ok:
                results["all_pass"] = False
            results["checks"].append({
                "name": f"{{gamma^{a}, gamma^{b}}} = 2 delta^{{{a}{b}}}",
                "error": float(err),
                "pass": ok,
            })

    # Check 2: gamma_5 anticommutation
    for a in range(4):
        anticomm = gamma[a] @ gamma5 + gamma5 @ gamma[a]
        err = norm(anticomm)
        ok = err < HERMITIAN_TOL
        if not ok:
            results["all_pass"] = False
        results["checks"].append({
            "name": f"{{gamma^{a}, gamma_5}} = 0",
            "error": float(err),
            "pass": ok,
        })

    # Check 3: gamma_5^2 = I
    err = norm(gamma5 @ gamma5 - I4)
    ok = err < HERMITIAN_TOL
    if not ok:
        results["all_pass"] = False
    results["checks"].append({
        "name": "gamma_5^2 = I",
        "error": float(err),
        "pass": ok,
    })

    # Check 4: gamma_5 = -gamma^0 gamma^1 gamma^2 gamma^3 (our convention)
    g5_product = gamma[0] @ gamma[1] @ gamma[2] @ gamma[3]
    err = norm(gamma5 + g5_product)  # gamma_5 = -product in our basis choice
    ok = err < HERMITIAN_TOL
    if not ok:
        results["all_pass"] = False
    results["checks"].append({
        "name": "gamma_5 = -product of gammas (convention)",
        "error": float(err),
        "pass": ok,
    })

    # Check 5: [sigma^{ab}, gamma_5] = 0  -- THE KEY CHIRALITY THEOREM
    sigma = build_sigma_generators(gamma)
    max_sigma_violation = 0.0
    for a in range(4):
        for b in range(4):
            comm = sigma[a, b] @ gamma5 - gamma5 @ sigma[a, b]
            err = norm(comm)
            max_sigma_violation = max(max_sigma_violation, err)
            ok = err < HERMITIAN_TOL
            if not ok:
                results["all_pass"] = False
            results["checks"].append({
                "name": f"[sigma^{{{a}{b}}}, gamma_5] = 0",
                "error": float(err),
                "pass": ok,
            })

    results["max_sigma_gamma5_violation"] = float(max_sigma_violation)
    results["n_checks"] = len(results["checks"])
    results["n_pass"] = sum(1 for c in results["checks"] if c["pass"])

    return results


