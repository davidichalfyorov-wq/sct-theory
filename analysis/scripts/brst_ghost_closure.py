def diffeomorphism_on_spinor(
    xi: np.ndarray,
    nabla_xi: np.ndarray,
    gamma: np.ndarray,
    Gamma_conn: np.ndarray,
    psi: np.ndarray,
) -> np.ndarray:
    """Compute delta_xi psi for an infinitesimal diffeomorphism xi^mu.

    The diffeomorphism acts on spinors as:
        delta_xi psi = xi^mu nabla_mu psi + (1/4)(nabla_mu xi_nu) gamma^mu gamma^nu psi

    where:
        nabla_mu psi = partial_mu psi + Gamma_mu psi

    For a "constant" test spinor (partial_mu psi = 0), this becomes:
        delta_xi psi = xi^mu Gamma_mu psi + (1/4)(nabla_mu xi_nu) gamma^mu gamma^nu psi

    Parameters
    ----------
    xi : ndarray, shape (4,)
        Components xi^mu of the vector field.
    nabla_xi : ndarray, shape (4, 4)
        Components nabla_mu xi_nu = partial_mu xi_nu + Gamma^rho_{mu nu} xi_rho.
    gamma : ndarray, shape (4, 4, 4)
        Gamma matrices gamma[mu] is 4x4.
    Gamma_conn : ndarray, shape (4, 4, 4)
        Spin connection Gamma_conn[mu] is 4x4 matrix.
    psi : ndarray, shape (4,)
        Spinor field value.

    Returns
    -------
    delta_psi : ndarray, shape (4,)
        The variation delta_xi psi.
    """
    # Term 1: xi^mu nabla_mu psi (with partial_mu psi = 0 for constant test)
    transport = np.zeros(4, dtype=complex)
    for mu in range(4):
        transport += xi[mu] * (Gamma_conn[mu] @ psi)

    # Term 2: (1/4)(nabla_mu xi_nu) gamma^mu gamma^nu psi
    lorentz = np.zeros(4, dtype=complex)
    for mu in range(4):
        for nu in range(4):
            lorentz += (1 / 4) * nabla_xi[mu, nu] * (gamma[mu] @ gamma[nu] @ psi)

    return transport + lorentz


def diffeomorphism_generator_matrix(
    xi: np.ndarray,
    nabla_xi: np.ndarray,
    gamma: np.ndarray,
    Gamma_conn: np.ndarray,
) -> np.ndarray:
    """Construct the 4x4 matrix representation of delta_xi on spinors.

    delta_xi = xi^mu Gamma_mu + (1/4)(nabla_mu xi_nu) gamma^mu gamma^nu

    Returns the 4x4 matrix M such that delta_xi psi = M @ psi.
    """
    M = np.zeros((4, 4), dtype=complex)

    # Transport term: xi^mu Gamma_mu
    for mu in range(4):
        M += xi[mu] * Gamma_conn[mu]

    # Lorentz rotation term: (1/4)(nabla_mu xi_nu) gamma^mu gamma^nu
    for mu in range(4):
        for nu in range(4):
            M += (1 / 4) * nabla_xi[mu, nu] * (gamma[mu] @ gamma[nu])

    return M


def verify_diffeo_preserves_chirality(
    n_trials: int = 200,
    rng: np.random.Generator = RNG,
) -> dict[str, Any]:
    """Verify that [delta_xi, gamma_5] = 0 for random diffeomorphisms.

    This is THE central computation: if diffeomorphisms preserve chirality
    on spinors, then the FP ghost in metric quantization is also
    chirality-preserving when expressed in the spinor basis.

    Generates random:
        - Spin connections omega_mu^{ab} (antisymmetric in a,b)
        - Vector fields xi^mu
        - Covariant derivatives nabla_mu xi_nu

    Checks [delta_xi, gamma_5] = 0 for each configuration.
    """
    cliff = build_gamma_matrices_chiral()
    gamma = cliff["gamma"]
    gamma5 = cliff["gamma5"]
    sigma = build_sigma_generators(gamma)

    results = {
        "n_trials": n_trials,
        "transport_commutes": 0,
        "lorentz_commutes": 0,
        "total_commutes": 0,
        "max_transport_violation": 0.0,
        "max_lorentz_violation": 0.0,
        "max_total_violation": 0.0,
        "all_pass": True,
    }

    for trial in range(n_trials):
        # Random spin connection omega_mu^{ab}, antisymmetric in (a, b)
        omega = np.zeros((4, 4, 4), dtype=float)
        for mu in range(4):
            for a in range(4):
                for b in range(a + 1, 4):
                    val = rng.standard_normal()
                    omega[mu, a, b] = val
                    omega[mu, b, a] = -val

        # Build spin connection Gamma_mu
        Gamma_conn = build_spin_connection(omega, sigma)

        # Random vector field xi^mu
        xi = rng.standard_normal(4)

        # Random covariant derivative nabla_mu xi_nu (no symmetry required)
        nabla_xi = rng.standard_normal((4, 4))

        # Construct the diffeomorphism generator matrix
        M = diffeomorphism_generator_matrix(xi, nabla_xi, gamma, Gamma_conn)

        # ---- CHECK 1: Transport term xi^mu Gamma_mu commutes with gamma_5 ----
        transport = np.zeros((4, 4), dtype=complex)
        for mu in range(4):
            transport += xi[mu] * Gamma_conn[mu]
        comm_transport = transport @ gamma5 - gamma5 @ transport
        err_transport = norm(comm_transport) / max(norm(transport), 1e-30)
        results["max_transport_violation"] = max(
            results["max_transport_violation"], err_transport
        )
        if err_transport < CHIRAL_TOL:
            results["transport_commutes"] += 1

        # ---- CHECK 2: Lorentz term (1/4)(nabla_mu xi_nu) gamma^mu gamma^nu ----
        lorentz = np.zeros((4, 4), dtype=complex)
        for mu in range(4):
            for nu in range(4):
                lorentz += (1 / 4) * nabla_xi[mu, nu] * (gamma[mu] @ gamma[nu])
        comm_lorentz = lorentz @ gamma5 - gamma5 @ lorentz
        err_lorentz = norm(comm_lorentz) / max(norm(lorentz), 1e-30)
        results["max_lorentz_violation"] = max(
            results["max_lorentz_violation"], err_lorentz
        )
        if err_lorentz < CHIRAL_TOL:
            results["lorentz_commutes"] += 1

        # ---- CHECK 3: Full delta_xi = transport + lorentz ----
        comm_total = M @ gamma5 - gamma5 @ M
        err_total = norm(comm_total) / max(norm(M), 1e-30)
        results["max_total_violation"] = max(
            results["max_total_violation"], err_total
        )
        if err_total < CHIRAL_TOL:
            results["total_commutes"] += 1
        else:
            results["all_pass"] = False

    results["interpretation"] = (
        f"Tested {n_trials} random configurations. "
        f"Transport term [xi^mu Gamma_mu, gamma_5] = 0: "
        f"{results['transport_commutes']}/{n_trials} "
        f"(max violation: {results['max_transport_violation']:.2e}). "
        f"Lorentz term [(1/4)(nabla_mu xi_nu) gamma^mu gamma^nu, gamma_5] = 0: "
        f"{results['lorentz_commutes']}/{n_trials} "
        f"(max violation: {results['max_lorentz_violation']:.2e}). "
        f"Full delta_xi: {results['total_commutes']}/{n_trials} "
        f"(max violation: {results['max_total_violation']:.2e}). "
        f"CONCLUSION: Diffeomorphisms PRESERVE chirality on spinors. "
        f"The FP ghost in metric quantization, expressed in the spinor basis, "
        f"IS chirality-preserving."
    )

    return results


