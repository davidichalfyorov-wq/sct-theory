def build_gamma_matrices_chiral() -> dict[str, np.ndarray]:
    """Construct 4D gamma matrices in the chiral (Weyl) basis.

    Convention (Euclidean signature for Seeley-DeWitt, standard for SCT):
        gamma^0 = [[0, I], [I, 0]]
        gamma^k = [[0, -i sigma_k], [i sigma_k, 0]]  for k=1,2,3
        gamma_5 = [[I, 0], [0, -I]]

    These satisfy:
        {gamma^a, gamma^b} = 2 delta^{ab}   (Euclidean)
        {gamma^a, gamma_5} = 0  for all a
        gamma_5^2 = I
    """
    I2 = np.eye(2, dtype=complex)
    sigma_1 = np.array([[0, 1], [1, 0]], dtype=complex)
    sigma_2 = np.array([[0, -1j], [1j, 0]], dtype=complex)
    sigma_3 = np.array([[1, 0], [0, -1]], dtype=complex)
    sigma = [sigma_1, sigma_2, sigma_3]

    gamma = np.zeros((4, 4, 4), dtype=complex)

    # gamma^0
    gamma[0] = np.block([[np.zeros((2, 2)), I2],
                          [I2, np.zeros((2, 2))]])

    # gamma^k for k = 1, 2, 3
    for k in range(3):
        gamma[k + 1] = np.block([[np.zeros((2, 2)), -1j * sigma[k]],
                                  [1j * sigma[k], np.zeros((2, 2))]])

    # gamma_5: in our convention we define gamma_5 = diag(+I, -I).
    # The product gamma^0 gamma^1 gamma^2 gamma^3 gives diag(-I, +I) in this basis,
    # so gamma_5 = -gamma^0 gamma^1 gamma^2 gamma^3.
    # Both conventions satisfy {gamma^a, gamma_5} = 0 and gamma_5^2 = I.
    gamma5 = np.block([[I2, np.zeros((2, 2))],
                        [np.zeros((2, 2)), -I2]])
    gamma5_product = gamma[0] @ gamma[1] @ gamma[2] @ gamma[3]

    gamma5_expected = np.block([[I2, np.zeros((2, 2))],
                                 [np.zeros((2, 2)), -I2]])

    return {
        "gamma": gamma,
        "gamma5": gamma5,
        "gamma5_product": gamma5_product,
        "gamma5_expected": gamma5_expected,
        "sigma_pauli": sigma,
        "I2": I2,
    }


def build_sigma_generators(gamma: np.ndarray) -> np.ndarray:
    """Construct spin connection generators sigma^{ab} = (i/4)[gamma^a, gamma^b].

    Returns sigma[a, b] as a (4, 4, 4, 4) array.
    sigma[a, b] is a 4x4 matrix.
    """
    sigma = np.zeros((4, 4, 4, 4), dtype=complex)
    for a in range(4):
        for b in range(4):
            sigma[a, b] = (1j / 4) * (gamma[a] @ gamma[b] - gamma[b] @ gamma[a])
    return sigma


def build_spin_connection(omega_components: np.ndarray,
                          sigma: np.ndarray) -> np.ndarray:
    """Construct spin connection Gamma_mu = (1/4) omega_mu^{ab} sigma_{ab}.

    In our convention with sigma_{ab} = (i/4)[gamma_a, gamma_b], the
    full connection is Gamma_mu = (1/4) omega_mu^{ab} gamma_a gamma_b.
    But equivalently, since sigma_{ab} = (i/4)(gamma_a gamma_b - gamma_b gamma_a),
    we have Gamma_mu = (1/2) omega_mu^{ab} sigma_{ab} (using antisymmetry of omega).

    Parameters
    ----------
    omega_components : ndarray, shape (4, 4, 4)
        omega[mu, a, b] = omega_mu^{ab}, antisymmetric in (a, b).
    sigma : ndarray, shape (4, 4, 4, 4)
        sigma[a, b] = (i/4)[gamma^a, gamma^b].

    Returns
    -------
    Gamma : ndarray, shape (4, 4, 4)
        Gamma[mu] is a 4x4 matrix (the spin connection 1-form).
    """
    Gamma = np.zeros((4, 4, 4), dtype=complex)
    for mu in range(4):
        for a in range(4):
            for b in range(4):
                Gamma[mu] += (1 / 2) * omega_components[mu, a, b] * sigma[a, b]
    return Gamma


