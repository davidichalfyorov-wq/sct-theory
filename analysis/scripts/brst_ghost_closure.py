    print("=" * 70)

    cliff = build_gamma_matrices_chiral()
    cliff_res = verify_clifford_algebra(cliff["gamma"], cliff["gamma5"])
    all_results["clifford"] = cliff_res

    # Verify gamma_5 is the expected form
    g5_err = norm(cliff["gamma5"] - cliff["gamma5_expected"])
    v.check_value("gamma_5 form", g5_err, 0.0, atol=1e-14)

    v.check_value("Clifford checks pass", cliff_res["n_pass"], cliff_res["n_checks"], atol=0)
    v.check_value("[sigma^ab, gamma_5] max violation",
                  cliff_res["max_sigma_gamma5_violation"], 0.0, atol=1e-14)

    print(f"  {cliff_res['n_pass']}/{cliff_res['n_checks']} checks pass")
    print(f"  Max [sigma^ab, gamma_5] violation: {cliff_res['max_sigma_gamma5_violation']:.2e}")

